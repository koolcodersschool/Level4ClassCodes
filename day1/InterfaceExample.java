package day1;

public interface InterfaceExample {
	
	/*
	 * Interface is not a class.
	 * We can NOT create object out of interface
	 * There are some differences between interface and abstract class
	 * 1 - Class level differences:
	 * 	   1.1 - Abstract class can extend only one class, but abstract class can
	 * implement many interfaces
	 * 	   1.2 - Abstract class can have constructor, but interface can NOT have constructor
	 * 
	 * 2 - Method level differences:
	 * 		2.1 - Abstract class can have abstract methods and other methods that regular class
	 * can have, But Interface can have only abstract methods, default methods and static methods
	 * 		2.2 - We can use "default" methods inside interface only, we can NOT create default 
	 * methods inside abstract or some other classes.
	 * 
	 * 3 - Variable level differences:
	 * 		3.1 - Abstract class can have any type of variables, but interface can have
	 * only public static final variables which we call constants.
	 * 
	 * 
	 * 
	 */

}
